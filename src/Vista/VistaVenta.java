    private void listarVentas() {
    DefaultTableModel modelo = (DefaultTableModel) tablaVentas.getModel();
    modelo.setRowCount(0); // limpiar tabla

    List<Venta> ventas = controlador.listarVentas();
    for (Venta v : ventas) {
        modelo.addRow(new Object[]{
            v.getId_Ventas(),
            v.getId_Clientes(),
            v.getCantidad_Pro(),
            v.getFe_Venta()
        });
    }
}
    
