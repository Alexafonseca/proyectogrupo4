    private void jComboClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboClienteActionPerformed

        String idSeleccionado = (String) jComboCliente.getSelectedItem();
        if (idSeleccionado != null && !idSeleccionado.isEmpty()) {
            try {
                int idCliente = Integer.parseInt(idSeleccionado);
                Cliente cliente = DAOCliente.obtenerClientePorId(idCliente);
                if (cliente != null) {
                    // Mostrar los detalles del cliente en un JOptionPane
                    String mensaje = "ID: " + cliente.getIdCliente() + "\n" +
                                   "Nombre: " + cliente.getNombre1() + " " + cliente.getNombre2() + "\n" +
                                   "Apellido: " + cliente.getApellido1() + " " + cliente.getApellido2() + "\n" +
                                   "Dirección: " + cliente.getDireccion() + "\n" +
                                   "Teléfono: " + cliente.getTelefono();
                    JOptionPane.showMessageDialog(this, mensaje, "Detalles del Cliente", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Cliente no encontrado", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Error al obtener cliente: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Seleccione un ID válido", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Por favor seleccione un cliente", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    
        // TODO add your handling code here:
    }//GEN-LAST:event_jComboClienteActionPerformed


